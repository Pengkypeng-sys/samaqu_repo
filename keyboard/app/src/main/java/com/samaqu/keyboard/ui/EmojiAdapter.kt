class EmojiAdapter(private val onClick: (String) -> Unit)
    : RecyclerView.Adapter<EmojiAdapter.VH>() {

    private var current: List<String> = EMOJI_CATEGORIES.values.first()

    fun setCategory(emojis: List<String>) {
        current = emojis
        notifyDataSetChanged()
    }

    inner class VH(val tv: TextView) : RecyclerView.ViewHolder(tv)

    override fun onCreateViewHolder(parent: ViewGroup, viewType: Int): VH {
        val size = (parent.context.resources.displayMetrics.density * 44).toInt()
        val tv = TextView(parent.context).apply {
            textSize = 22f
            gravity = Gravity.CENTER
            layoutParams = ViewGroup.LayoutParams(size, size)
        }
        return VH(tv)
    }

    override fun onBindViewHolder(h: VH, pos: Int) {
        h.tv.text = current[pos]
        h.tv.setOnClickListener { onClick(current[pos]) }
    }

    override fun getItemCount() = current.size
}
